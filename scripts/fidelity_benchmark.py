"""Fidelity benchmark for the Sūtrakṛt translation panel.

For each verse where (a) we have generated 6 school-anchored renderings
and (b) supersite has external English translations, compute three
metrics:

  1. SUPERSET COVERAGE — does the union of our 6 renderings cover the
     semantic content of each external translator? Per supersite-translator,
     what fraction of their content-bearing tokens has a semantic match
     somewhere in our panel?

  2. CROSS-SCHOOL DIVERGENCE — are our 6 renderings actually distinct,
     or do they collapse to paraphrases of one reading? Pairwise mE5
     cosine distance averaged across all (school_i, school_j) pairs.
     Higher = better.

  3. CONTAMINATION CHECK — are our renderings echoing supersite's
     phrasing (which would mean the subagent leaked training-data English
     Gītā memory)? Longest common substring, n-gram overlap. Lower =
     better (we want our work to be anchored in bhāṣya prose, not in
     Gambhīrānanda's English).

Usage:
    python scripts/fidelity_benchmark.py --chapter 2

Output: data/fidelity_reports/bg2_fidelity_report.json + console summary.
"""

from __future__ import annotations

import argparse
import json
import re
import sys
from collections import Counter
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(REPO_ROOT))

OUR_RESULTS_DIR = Path("/tmp/bg2_translations")  # generated by subagents
SUPERSITE_PATH = REPO_ROOT / "data" / "external_translations" / "supersite_bg2.json"
REPORT_DIR = REPO_ROOT / "data" / "fidelity_reports"

# Stopwords kept lean — content words drive coverage.
STOPWORDS = set("""
a an the and or but if then so for to of in on at by with from as is are was were be been being am
this that these those it its his her hers their them they him he she we us our you your i my mine
not no nor too very can may might shall should will would could do does did done has have had
which who whom whose what when where why how all any some many much each every other another
into out up down upper lower over under more less most least just still even also already only
than there here now thus also well good well-known said say says like such only do not don't
""".split())

# Highly distinctive Sanskrit term lemmas that, if they appear in BOTH our and theirs,
# don't count as contamination — they're the verse's actual content.
ALLOWED_SHARED_VOCAB = {
    "atman", "brahman", "krsna", "krishna", "arjuna", "bhagavan", "yoga", "karma",
    "dharma", "moksha", "samkhya", "purusha", "prakriti", "guna", "veda",
    "vedanta", "advaita", "dvaita", "ishvara", "deva", "bhakti", "jnana",
}


def normalize(text: str) -> str:
    """Lowercase, strip diacritics for matching, normalize whitespace."""
    if not text:
        return ""
    text = text.lower()
    # Strip parenthesized glosses for the substring check (we keep them in display).
    text = re.sub(r"\([^)]*\)", " ", text)
    # Strip punctuation
    text = re.sub(r"[^\w\s'-]", " ", text)
    # Collapse whitespace
    text = re.sub(r"\s+", " ", text).strip()
    return text


def content_tokens(text: str) -> list[str]:
    norm = normalize(text)
    return [t for t in norm.split() if t not in STOPWORDS and len(t) > 2]


def jaccard(a: set, b: set) -> float:
    if not a and not b:
        return 1.0
    if not a or not b:
        return 0.0
    return len(a & b) / len(a | b)


def superset_coverage_one(
    our_panel_text: str,
    external_text: str,
) -> float:
    """Fraction of external content tokens that appear (lemma-loose) in our panel."""
    our_tokens = set(content_tokens(our_panel_text))
    their_tokens = content_tokens(external_text)
    if not their_tokens:
        return 1.0
    matched = sum(1 for t in their_tokens if t in our_tokens)
    return matched / len(their_tokens)


def cross_school_divergence(renderings: dict[str, str]) -> float:
    """Mean pairwise Jaccard *distance* across the 6 school-renderings.
    Higher = more divergent = better airgap discipline."""
    schools = list(renderings.keys())
    token_sets = {s: set(content_tokens(renderings[s])) for s in schools}
    distances = []
    for i, s1 in enumerate(schools):
        for s2 in schools[i + 1:]:
            sim = jaccard(token_sets[s1], token_sets[s2])
            distances.append(1.0 - sim)
    return sum(distances) / len(distances) if distances else 0.0


def longest_common_word_run(a: str, b: str) -> int:
    """Longest run of consecutive matching content words. Detects copy-paste."""
    aw = content_tokens(a)
    bw = content_tokens(b)
    if not aw or not bw:
        return 0
    # Strip allowed shared vocab — those don't count
    aw = [w for w in aw if w not in ALLOWED_SHARED_VOCAB]
    bw_set_neighbors = {}
    for i, w in enumerate(bw):
        if w in ALLOWED_SHARED_VOCAB:
            continue
        bw_set_neighbors.setdefault(w, []).append(i)
    best = 0
    for i, w in enumerate(aw):
        for j in bw_set_neighbors.get(w, []):
            run = 0
            while (i + run) < len(aw) and (j + run) < len(bw) and aw[i + run] == bw[j + run]:
                run += 1
            if run > best:
                best = run
    return best


def _renderings_dict(result: dict) -> dict[str, str]:
    """Normalize doctrinal_renderings to {school: english_text}.
    Some subagents emitted a list-of-dicts instead of dict-of-dicts; handle both."""
    drs = result.get("doctrinal_renderings")
    if drs is None:
        return {}
    out: dict[str, str] = {}
    if isinstance(drs, dict):
        for school, v in drs.items():
            if isinstance(v, dict):
                out[school] = v.get("english", "") or ""
            else:
                out[school] = str(v)
    elif isinstance(drs, list):
        for entry in drs:
            if not isinstance(entry, dict):
                continue
            school = entry.get("school") or entry.get("name") or ""
            text = entry.get("english", "") or entry.get("rendering", "") or ""
            if school:
                out[school] = text
    return out


def panel_text(result: dict) -> str:
    """Concatenate all 6 doctrinal renderings into one text for coverage check."""
    return " ".join(_renderings_dict(result).values())


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--chapter", default="2", help="Chapter to benchmark.")
    args = parser.parse_args()

    REPORT_DIR.mkdir(parents=True, exist_ok=True)
    supersite = json.loads(SUPERSITE_PATH.read_text())["verses"]

    per_verse: list[dict] = []
    coverage_by_translator: dict[str, list[float]] = {}
    contamination_by_translator: dict[str, list[int]] = {}
    divergence_scores: list[float] = []

    for results_file in sorted(OUR_RESULTS_DIR.glob("verse_*_*.results.json")):
        try:
            ours = json.loads(results_file.read_text())
        except Exception:
            continue
        vid = ours.get("verse_id", "")
        if not vid.startswith(f"{args.chapter}."):
            continue
        external = supersite.get(vid, {}).get("translations", {})
        if not external:
            continue

        our_text = panel_text(ours)
        renderings = _renderings_dict(ours)
        divergence = cross_school_divergence(renderings)
        divergence_scores.append(divergence)

        per_translator = {}
        for tname, ttext in external.items():
            cov = superset_coverage_one(our_text, ttext)
            contam = longest_common_word_run(our_text, ttext)
            per_translator[tname] = {"coverage": cov, "longest_run": contam}
            coverage_by_translator.setdefault(tname, []).append(cov)
            contamination_by_translator.setdefault(tname, []).append(contam)

        per_verse.append({
            "verse_id": vid,
            "cross_school_divergence": divergence,
            "external_translators_compared": len(external),
            "per_translator": per_translator,
        })

    def avg(xs: list[float]) -> float:
        return sum(xs) / len(xs) if xs else 0.0

    summary = {
        "chapter": args.chapter,
        "verses_evaluated": len(per_verse),
        "mean_cross_school_divergence": avg(divergence_scores),
        "mean_coverage_by_translator": {
            t: avg(v) for t, v in coverage_by_translator.items()
        },
        "mean_longest_run_by_translator": {
            t: avg(v) for t, v in contamination_by_translator.items()
        },
        "max_longest_run_by_translator": {
            t: max(v) if v else 0 for t, v in contamination_by_translator.items()
        },
    }

    report = {"summary": summary, "per_verse": per_verse}
    out = REPORT_DIR / f"bg{args.chapter}_fidelity_report.json"
    out.write_text(json.dumps(report, ensure_ascii=False, indent=2))

    print(f"\nFIDELITY BENCHMARK — BG chapter {args.chapter}")
    print(f"Verses evaluated: {summary['verses_evaluated']}")
    print(f"\nCross-school divergence (higher = better, 0.0–1.0):")
    print(f"  mean: {summary['mean_cross_school_divergence']:.3f}")
    print(f"\nSuperset coverage per supersite translator (higher = better):")
    for t, cov in summary["mean_coverage_by_translator"].items():
        print(f"  {t}: {cov:.3f}")
    print(f"\nLongest copied run per supersite translator (lower = better — content words only):")
    for t, run in summary["mean_longest_run_by_translator"].items():
        max_run = summary["max_longest_run_by_translator"][t]
        print(f"  {t}: mean {run:.2f}, max {max_run}")
    print(f"\nReport: {out}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
